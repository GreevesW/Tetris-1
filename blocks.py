
#defines the rotation positions for all block types

from block import Block
from position import Position

#Defines all of the coordinates for each of the rotational states for LBlocks
#Positions refere to coordinates in a grid that link to an objects rotational state
class LBlock(Block):
	def __init__(self):
		super().__init__(id = 1)
		self.cells = {
			0: [Position(0, 2), Position(1, 0), Position(1, 1), Position(1, 2)],
			1: [Position(0, 1), Position(1, 1), Position(2, 1), Position(2, 2)],
			2: [Position(1, 0), Position(1, 1), Position(1, 2), Position(2, 0)],
			3: [Position(0, 0), Position(0, 1), Position(1, 1), Position(2, 1)]
		}
		self.move(0, 3)
		#makes all blocks appear at the centre of the screen by default

